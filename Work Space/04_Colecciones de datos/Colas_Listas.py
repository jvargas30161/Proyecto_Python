## Se usa paquete de colecciones deque

from collections import deque

cola = deque(["Alex","Juan","Julio","Jorge","Nicolas","Vargas","Toribio","Andrea","Torino","Ferran","jajajaj",])
cola.append("Robino")   #con esta función está añadiendo el nombre Robino en el ultimo lugar.
cola.popleft()          #con esta función está eliminando el primer nombre o valor en este caso es Alex.
print (cola)




