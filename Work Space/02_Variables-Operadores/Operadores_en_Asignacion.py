print ("========Multiplicación en asignación======")
a *= 2
print (a)

print ("==========División en asignación==========")
a /= 2
print (a)

a //= 10
print (a)


print ("==========Poetencia en asignación==========")
a **= 2
print (a)


print ("==========Modulo en asignación==========")
a %= 3
