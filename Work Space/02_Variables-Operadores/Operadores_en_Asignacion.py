## estos operadores simplifican algunas operaciones aritméticas.
print ("=========== Valor simple de A ==========")
a = 10
print (a)

print ("=========== se suma + 10 a A ==========")
a = a + 10
print (a)
print ("========================================")

print ("============suma en asignación==========")
a += 15
print (a)
##resta en asignación
print ("============resta en asignación==========")
